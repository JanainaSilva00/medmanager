    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Medico f = new Medico();
        if(tbMedico.getSelectedRows().length==0){
            f.setNome(txtNome.getText());
            f.setCrm(txtCrm.getText());
            lstMedico.add(f);
            txtNome.setText("");
            txtCrm.setText("");
        }else{
            lstMedico.add(f);
            tbMedico.getSelectionModel().setSelectionInterval(
                    lstMedico.size()-1, 
                    lstMedico.size()-1);
        }
        
    }//GEN-LAST:event_btnAdicionarActionPerformed

